/**
 * 將中文字串轉換為帶注音的 HTML
 * @param {string} text - 原始中文文字
 * @returns {string} - 帶有 <ruby> 標籤的 HTML 字串
 */
export function toBopomofoHTML(text) {
    if (!text) return '';

    let result = '';
    for (const char of text) {
        const bopomofo = bopomofoDict[char];
        if (bopomofo) {
            result += `<ruby>${char}<rt>${bopomofo}</rt></ruby>`;
        } else {
            result += char;
        }
    }
    return result;
}

/**
 * 取得單個字的注音
 * @param {string} char - 單個中文字
 * @returns {string|null} - 注音或 null
 */
export function getBopomofo(char) {
    return bopomofoDict[char] || null;
}

/**
 * 檢查字典是否有該字
 */
export function hasBopomofo(char) {
    return char in bopomofoDict;
}

